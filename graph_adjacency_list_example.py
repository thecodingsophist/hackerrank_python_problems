# There are different ways of implementing an adjacency list:
# 1) HashTable: each key is the vertex, value associated is the list/array of adjacent vertices
# 2) Index Numbers: Array cell where index is the head vertex, and linked vertices are in a singly linked list
# 3) Object Oriented: Each vertex has an instance variable pointing to a colleciton of vertices, and each edge is associated with two vertices
